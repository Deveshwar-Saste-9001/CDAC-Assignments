// 19
// 13

public class Asq4 {
    public static void main(String args[]) {
        int a, b, c, d;
        a = -5 + 8 * 6;
        b = (55 + 9) % 9;
        c = 20 + -3 * 5 / 8;
        d = 5 + 15 / 3 * 2 - 8 % 3;
        System.out.println("Output for -5 + 8 * 6 is :- " + a);
        System.out.println("Output for (55+9) % 9 is :- " + b);
        System.out.println("Output for 20 + -3*5 / 8:- " + c);
        System.out.println("Output for 5 + 15 / 3 * 2 - 8 % 3 is :- " + d);

    }

}
